package com.takeo.task.challenge1;

public class CompileAllInOne {
    public static void main(String[] args) {
        // Challenge 1: Declare different types of variables
        int age = 30;
        double salary = 1234.53;
        char gender = 'M';
        String name = "Niraj Thagunna";
        boolean isActive = true;

        System.out.println("Challenge 1 - Declare Variables:");
        System.out.println("Name: " + name);
        System.out.println("Age: " + age);
        System.out.println("Gender: " + gender);
        System.out.println("Salary: " + salary);
        System.out.println("Active: " + isActive);
        System.out.println();

        // Challenge 2: Sum of Two Numbers
        int num1 = 10;
        int num2 = 20;
        int sum = num1 + num2;

        System.out.println("Challenge 2 - Sum of Two Numbers:");
        System.out.println("Sum: " + sum);
        System.out.println();

        // Challenge 3: Subtraction of Two Numbers
        int num3 = 30;
        int num4 = 15;
        int resultSub = num3 - num4;

        System.out.println("Challenge 3 - Subtraction of Two Numbers:");
        System.out.println("Subtraction: " + resultSub);
        System.out.println();

        // Challenge 4: Multiplication of Two Numbers
        int num5 = 2;
        int num6 = 8;
        int product = num5 * num6;

        System.out.println("Challenge 4 - Multiplication of Two Numbers:");
        System.out.println("Multiplication: " + product);
        System.out.println();

        // Challenge 5: Division of Two Numbers
        double num7 = 20;
        double num8 = 10;
        double resultDiv = num7 / num8;

        System.out.println("Challenge 5 - Division of Two Numbers:");
        System.out.println("Division: " + resultDiv);
    }
}
