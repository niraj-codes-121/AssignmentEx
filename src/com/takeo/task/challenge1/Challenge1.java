package com.takeo.task.challenge1;
//1) Challenge: Declare different type of variables
//   Description: Write a program to declare vriables
//   and print their values.(Hello World Program)
public class Challenge1 {
    public static void main(String[] args) {
            // Integer variable
            int age = 30;

            // Floating-point variable,double
            double salary = 1234.53;

            // Character variable
            char gender = 'M';

            // String variable
            String name = "Niraj Thagunna";

            // Boolean variable
            boolean isActive = true;

            // Print values
            System.out.println("Name: " + name);
            System.out.println("Age: " + age);
            System.out.println("Gender: " + gender);
            System.out.println("Salary: " + salary);
            System.out.println("Active: " + isActive);
        }
    }

