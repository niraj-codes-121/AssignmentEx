package com.takeo.task.challenge1;
//Challenge: Subtraction of Two Numbers
//   Description: Write a program that calculates the
//   subtraction of two numbers and prints the result.
public class Challenge3 {
    public static void main(String[] args) {
        // Declare two numbers
        int num1 = 30;
        int num2 = 15;

        // Calculate the subtraction
        int result = num1 - num2;

        // Print the result
        System.out.println("Subtraction of " + num1 + " and " + num2 + " is: " + result);
    }
}
