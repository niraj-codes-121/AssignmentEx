package com.takeo.task.challenge1;
// Challenge: Multiplication of Two Numbers
//   Description: Write a program that calculates
//   the multiplication of two numbers and prints the result.
public class Challenge4 {
    public static void main(String[] args) {
        // Declare two numbers
        int num1 = 2;
        int num2 = 8;

        // Calculate the multiplication
        int product = num1 * num2;

        // Print the result
        System.out.println("Multiplication of " + num1 + " and " + num2 + " is: " + product);
    }
}
