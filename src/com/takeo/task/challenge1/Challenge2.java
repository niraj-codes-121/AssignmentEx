package com.takeo.task.challenge1;
//2. Challenge: Sum of Two Numbers
//   Description: Write a program that calculates the
//   sum of two numbers and prints the result.
public class Challenge2 {
    public static void main(String[] args) {
        // Declare two numbers
        int num1 = 10;
        int num2 = 20;

        // Calculate the sum
        int sum = num1 + num2;

        // Print the result
        System.out.println("Sum of " + num1 +
                " and " + num2 + " is: " + sum);
    }
}
